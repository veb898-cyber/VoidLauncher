import { useAuthStore } from '../stores/authStore';
import { GameRunningBadge } from './launch/GameRunningBadge';
import { t } from '../lib/i18n';

interface SidebarProps {
  activePage: string;
  onNavigate: (page: string) => void;
}

export function Sidebar({ activePage, onNavigate }: SidebarProps) {
  const { profile, isLoggedIn } = useAuthStore();

  const navItems = [
    { id: 'home', icon: <HomeIcon />, label: t('sidebar.home') },
    { id: 'game_logs', icon: <GameLogsIcon />, label: t('sidebar.game_logs') },
    { id: 'instances', icon: <InstancesIcon />, label: t('sidebar.instances') },
    { id: 'accounts', icon: <AccountsIcon />, label: t('sidebar.accounts') },
    { id: 'logs', icon: <LogsIcon />, label: t('sidebar.logs') },
    { id: 'settings', icon: <SettingsIcon />, label: t('sidebar.settings') },
  ];

  return (
    <nav className="sidebar">
      <div className="sidebar__nav">
        {navItems.map((item) => (
          <button
            key={item.id}
            className={`sidebar__item ${activePage === item.id ? 'sidebar__item--active' : ''}`}
            onClick={() => onNavigate(item.id)}
            title={item.label}
          >
            {item.icon}
          </button>
        ))}
      </div>

      <div className="sidebar__bottom">
        <GameRunningBadge />
        <div className="sidebar__separator" />
        {isLoggedIn && profile ? (
          <div
            className="sidebar__avatar"
            onClick={() => onNavigate('settings')}
            title={profile.name}
          >
            <img
              src={`https://mc-heads.net/avatar/${profile.id}/36`}
              referrerPolicy="no-referrer"
              alt={profile.name}
              onError={(e) => {
                (e.target as HTMLImageElement).src = `data:image/svg+xml,<svg xmlns="http://www.w3.org/2000/svg" width="36" height="36" viewBox="0 0 36 36"><rect fill="%23333" width="36" height="36" rx="6"/><text x="18" y="23" text-anchor="middle" fill="%23999" font-size="16">${profile.name.charAt(0).toUpperCase()}</text></svg>`;
              }}
            />
          </div>
        ) : (
          <button
            className="sidebar__item"
            onClick={() => onNavigate('login')}
            title={t('sidebar.login')}
          >
            <LoginIcon />
          </button>
        )}
      </div>
    </nav>
  );
}

